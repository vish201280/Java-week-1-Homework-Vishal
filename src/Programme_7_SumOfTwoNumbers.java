public class Programme_7_SumOfTwoNumbers {

    /**
     * 7. Write a Java program to print the sum of two numbers.
     * Test Data: 74 + 36
     * Expected Output: 110
     */

    public static class Programme7SumOfTwoNumbers {

        public static void main(String[] args) {
            //The two integers are value1 and value2
            int value1 = 74;
            int value2 = 36;
            int sum = value1 + value2;
            //concatenation
            System.out.println("The Sum of " + value1 + " and " + value2 + " is " + sum);
        }
    }

}

